class Ingredients {
    
    private double cupsOfSugar;
    private double cupsOfCream;
    private String addedFlavor;

    public double getCupsOfSugar() {
        return cupsOfSugar;
    }

    public void setCupsOfSugar(double cupsOfSugar) {
        this.cupsOfSugar = cupsOfSugar;
    }

    public double getCupsOfCream() {
        return cupsOfCream;
    }

    public void setCupsOfCream(double cupsOfCream) {
        this.cupsOfCream = cupsOfCream;
    }

    public String getAddedFlavor() {
        return addedFlavor;
    }

    public void setAddedFlavor(String addedFlavor) {
        this.addedFlavor = addedFlavor;
    }
    
}
